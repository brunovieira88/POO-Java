package javafullcourse;
public class operators {
    public static void main(String[] args) {
        int x = 10;
        int y = 2;
        x += y ; // x = x + y
        System.out.println(x);
        x -= y ; // x = x - y
        x *= y ; // x = x * y
        x /= y ; // x = x / y

        x++; // x = x+1 tha same with minus 
        
    }
}
